String getAvatarByInt(int i) {
  switch (i) {
    case 0:
      {
        return "assets/avatars/alien.png";
      }
    case 1:
      {
        return "assets/avatars/pride.png";
      }
    case 2:
      {
        return "assets/avatars/cat.png";
      }
    case 3:
      {
        return "assets/avatars/cow.png";
      }
    case 4:
      {
        return "assets/avatars/dog.png";
      }
    case 5:
      {
        return "assets/avatars/unicorn.png";
      }
    default:
      {
        return "assets/avatars/alien.png";
      }
  }
}
